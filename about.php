    </p>


        <h2 class="titre">1ère étape : Le dessin</h2>

        <p>
            J’étais dans une période assez créative. J’ai repris le dessin sur tablette pour essayer de dessiner des personnages. Souvent, l’inspiration me venait à 21h mais n'étant pas très douée pour représenter ce que j'avais en tête sur une feuille. Je finissais souvent au petit matin vers 9h du matin puis j'allais dormir. <br><br>
            Il m'était impossible d'aller me coucher tant que je n'avais pas réussi à extérioriser ce que j'avais en tête.
        </p>
    
        <h2 class="titre">2ème étape : L'animation de dessin</h2>

        <p>
            Après ma période de dessin statique je me suis mise en tête de faire de l'animation de dessin. <br>
            Je cherchais des vidéos sur internet pour comprendre comment cela fonctionnait ainsi que des logiciels pour m'excercer. <br> <br>

            Je me suis lancée avec le logiciel Krita, mais je me suis vite rendu compte que cela allait au-delà de mes capacités. Dans une seconde il y a 25 images. <br> <br>
            
            Ce qui veut dire qu'en 1 seconde j'aurai du dessiner mon personnages 25 fois. De plus j'ai du mal avec la géométrie dans l'espace. Reproduire deux même dessin avec des positions / angles différents m'est réellement compliqué. <br> <br>

            J'ai tout de même essayé avec des dessins simple, mais le résultat était peu concluant et j'ai un peu abandonné l'idée 



        </p>

        <h2 class="titre">3ème étape : L'animation 3D</h2>

        <p>
        Ne voulant pas abandonné mon projet, je continuais à regarder des vidéos jusqu'à tomber par hasard sur une vidéo de modélisation 3D.

        J'ai décidé de me lancé dans l'apprentissage autodidacte de celle-ci, en commençant par créer des objets simples comme une table, une chaise, une cuillère <br> <br>

        Puis vient le moment où je décide de récupéré un modèle en 3D de mon personnage de World of Warcraft et de le lier à un squelette pour pouvoir l'animer. <br>
        La question de la géométrie dans l'espace ne se posait plus. Maintenant c'était la crédibilité des poses et mouvements du personnage. <br>

       
        </p>

